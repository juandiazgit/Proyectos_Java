
public class TiposDeDatos {

	public static void main(String[] args) {

		System.out.println("\tEl tipo de datos: " + Byte.TYPE);
		System.out.println("\tEstá formado por: " + Byte.BYTES + " Bytes");
		System.out.println("\tUn byte está formado por: " + Byte.SIZE + " Bits");
		System.out.println("\tSu Rango va del: " + Byte.MIN_VALUE + " a " + Byte.MAX_VALUE);
		System.out.println("\t---------------------------------------------------------------------------------");
		System.out.println("\tRango byte desde:  " + Byte.MIN_VALUE + "\t" + "hasta:  " + Byte.MAX_VALUE);
		System.out.println("\t---------------------------------------------------------------------------------");
		System.out.println("\tRango short desde: " + Short.MIN_VALUE + "\thasta:  " + Short.MAX_VALUE);
		System.out.println("\t---------------------------------------------------------------------------------");
		System.out.println("\tRango int desde: " + Integer.MIN_VALUE + "\thasta:  " + Integer.MAX_VALUE);
		System.out.println("\t---------------------------------------------------------------------------------");
		System.out.println("\tRango long desde:  " + Long.MIN_VALUE + "\thasta:  " + Long.MAX_VALUE);
		System.out.println("\t---------------------------------------------------------------------------------");
		System.out.println("\tRango float desde:  " + Float.MIN_VALUE + "\thasta:  " + Float.MAX_VALUE);
		System.out.println("\t---------------------------------------------------------------------------------");
		System.out.println("\tRango double desde:  " + Double.MIN_VALUE + "\thasta:  " + Double.MAX_VALUE);
		System.out.println("\t---------------------------------------------------------------------------------");
	}

}
