
public class Manipula_Cadenas {

	public static void main(String[] args) {
		
		String nombre = "Guillermo Diaz";
		
		System.out.println("Longitud: "+nombre.length());
		System.out.println("Posición: "+nombre.charAt(5));
		System.out.println("Posición última: "+nombre.charAt(nombre.length()-1));
	}

}
